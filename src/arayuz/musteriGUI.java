    private void btn_musteri_islemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_musteri_islemActionPerformed
        // TODO add your handling code here:
        
        String miktar = String.valueOf(combo_musteri_islem.getSelectedItem()); // objeyi string yaptık
        
        if(fld_musteri_islem_ayyy.getText().length()==0 || fld_musteri_islem_yilll.getText().length()==0){
            
            JOptionPane.showMessageDialog(null,"Lutfen boslukları eksiksiz doldurunuz");
            
        }
        else{
            
            int ay = Integer.parseInt(fld_musteri_islem_ayyy.getText());
            int yil = Integer.parseInt(fld_musteri_islem_yilll.getText());
            
            if(ay<1 || ay>12){
                
                JOptionPane.showMessageDialog(null,"Lutfen gecerli  ay giriniz");
                
            }
            else{
                
                
                if(miktar.equals("Hepsi")){
                    
                    islemGoruntule(ay , yil);
                    JOptionPane.showMessageDialog(null,"Islem basarili bir sekilde gerceklesti");
                }
                else{
                    
                    islemMiktarGoruntule(miktar, ay , yil);
                    JOptionPane.showMessageDialog(null,"Islem basarili bir sekilde gerceklesti");
                }
                
            }
        }
    }//GEN-LAST:event_btn_musteri_islemActionPerformed

