    public void hesapGoruntule(){
        
        hesapModel.setRowCount(0);
        
        ArrayList <Hesap> hesaplar = new ArrayList <Hesap>();
        
        hesaplar = musteri.getHesapList(); // databasen gelen degerlerimizi aldık
        
        if(hesaplar != null){
            
            for(Hesap hesap : hesaplar){
                Object [] degerler ={hesap.getHno(),hesap.getHad(),hesap.getBakiye(),hesap.getPbirimiad()};
                
                hesapModel.addRow(degerler); //metodu object dizi aldıgından degerler dizisini object tanımladık
            }
        }
        
        
    }
    
