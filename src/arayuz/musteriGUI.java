    private void btn_musteri_aylik_borcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_musteri_aylik_borcActionPerformed
        // TODO add your handling code here:
        
        double miktar = Double.parseDouble(lbl_musteri_aylik_odenecek.getText());
        String tarih = lbl_musteri_tarih.getText();
        
        if(miktar!=0){
            
            boolean flag = musteri.borcOde(miktar , tarih);
            
            if(flag){
                JOptionPane.showMessageDialog(null,"Islem basarili bir sekilde gerceklesti");
                
                musteri.krediBilgileriGuncelle();  // borcun odendigine dair
                hesapGoruntule();
                hesapGoruntule2();      // framedeki tum hesap bilgilerini guncelleyelim ne olur neolmaz
                hesapTumGoruntule();
                krediGoruntule();
            }
            else{
                
                JOptionPane.showMessageDialog(null,"Bir hata ile karsilasildi");
                
            }
            
        }
        else{
              JOptionPane.showMessageDialog(null,"Aylik borc gozukmemektedir");
        }
        
        lbl_musteri_aylik_odenecek.setText("0");
        lbl_musteri_toplam_odenecek.setText("0");
    }//GEN-LAST:event_btn_musteri_aylik_borcActionPerformed

